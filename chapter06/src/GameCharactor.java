public class GameCharactor {
    String name;
    String job;
    String sex;
    int hp;
    int mp;
    int birthday;

    public GameCharactor(String name, String job, String sex, int hp, int mp, int birthday) {
        this.name = name;
        this.job = job;
        this.sex = sex;
        this.hp = hp;
        this.mp = mp;
        this.birthday = birthday;
    }
}
